# Math operations

print(1 + 1)  # 2
print(2 - 1)  # 1
print(2 * 2)  # 4
# Basic division
print(10 / 2)  # 5.0
# Integer division (excluding the remainder)
print(10 // 2)  # 5
# Modulus (remainder of the division)
print(5 % 2)  # 1
# Exponential
print(2 ** 3)  # 8
print(2 ** 4)  # 16
# Precedence
print((2 + 3) * 4)  # 20
print((10 - 5) * 2)  # 10
print(10 - 5 * 2)  # 0
print(10 ** 2 * 2)  # 200

# Comparison operators
print(10 > 5)  # True
print(10 < 5)  # False
print(10 == 10)  # True
print(10 != 10)  # False
print(10 >= 10)  # True
print(10 <= 10)  # True

# Logical operators
print(10 > 5 and 10 < 5)  # False
print(10 > 5 or 10 < 5)  # True
print(not 10 > 5)  # False
